import Foundation

// Float이 수용할 수 있는 범위를 넘어섭니다.
// 자신이 감당할 수 있는 만큼만 남기므로 정확도가 떨어집니다.
var floatValue: Float = 1234567890.1

// Double은 충분히 수용할 수 있습니다.
let doubleValue: Double = 1234567890.1
   
// floatValue = 123456.1

print("floatValue: \(floatValue) doubleValue : \(doubleValue)")



